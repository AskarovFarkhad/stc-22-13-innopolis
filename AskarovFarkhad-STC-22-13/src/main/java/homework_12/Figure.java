package homework_12;

public abstract class Figure {

    private double x;
    private double y;

    abstract void getPerimeter();

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
